def create_class_map():
    """Create class_map.json: maps model index (0-453) to folder name (1-454)"""
    class_map = {}
    for idx, char in enumerate(sinhala_classes):
        class_map[str(idx)] = {
            "folder_name": str(idx + 1),
            "character": char
        }

    os.makedirs("/Users/dev/PycharmProjects/PythonProject/sinhala_char_recognition/mappings", exist_ok=True)
    with open("/Users/dev/PycharmProjects/PythonProject/sinhala_char_recognition/mappings/class_map.json", "w", encoding="utf-8") as f:
        json.dump(class_map, f, ensure_ascii=False, indent=2)
    print(f"Created class_map.json with {len(class_map)} classes")


def create_unicode_map():
    """Create unicode_map.json with basic structure"""
    unicode_map = {}

    for idx, char in enumerate(sinhala_classes):
        class_id = idx + 1

        unicode_map[str(class_id)] = {
            "unicode": char,
            "class_id": class_id,
            "index": idx,
            "display_name": f"{char}",
            "category": "sinhala_character"
        }

    with open("/Users/dev/PycharmProjects/PythonProject/sinhala_char_recognition/mappings/unicode_map.json", "w", encoding="utf-8") as f:
        json.dump(unicode_map, f, ensure_ascii=False, indent=2)
    print(f"Created unicode_map.json with {len(unicode_map)} classes")


